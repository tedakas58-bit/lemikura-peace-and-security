// Language Manager
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('language') || 'am';
        this.init();
    }

    init() {
        this.updateLanguageButton();
        this.applyTranslations();
    }

    toggleLanguage() {
        this.currentLang = this.currentLang === 'am' ? 'en' : 'am';
        localStorage.setItem('language', this.currentLang);
        this.updateLanguageButton();
        this.applyTranslations();
        document.documentElement.lang = this.currentLang;
    }

    updateLanguageButton() {
        const langBtn = document.getElementById('currentLang');
        console.log('ï¿½ Uopdating language button, element found:', !!langBtn);
        if (langBtn) {
            const newText = this.currentLang === 'am' ? 'EN' : 'áŠ áˆ›';
            langBtn.textContent = newText;
            console.log('âœ… Language button updated to:', newText);
        } else {
            console.warn('âš ï¸ Language button element not found (currentLang)');
        }
    }

    translate(key) {
        return translations[this.currentLang][key] || key;
    }

    applyTranslations() {
        console.log('ğŸŒ Applying translations for language:', this.currentLang);
        // Update all elements with data-translate attribute
        const elements = document.querySelectorAll('[data-translate]');
        console.log('ğŸ“ Found', elements.length, 'elements to translate');
        
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });
        
        console.log('âœ… Translations applied');
    }
}

